package com.dl.one;

public class Eg1 {

	public static void main(String[] args) {
		// Arithmetic operator + - / * %
		int a = 10;
		int b = 20;
		int c = 30;
		
		System.out.println(a+b);
		System.out.println(a-c);
		System.out.println(c/a);
		System.out.println(b*c);
		System.out.println(a%c);
		
		int x = 15;
		int y = 30;
		
		System.out.println(x/y);
		System.out.println(x%y);
		
		

	}

}
