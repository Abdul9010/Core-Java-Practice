package com.dl.one;

public class Selection {

	public static void main(String[] args) {
		if (true) {
			
			System.out.println("true");
			
		}else {
			System.out.println("false");
		}
		
		int a = 100, b = 200;
		if (a>b) {
			System.out.println("false");
			
		}else {
			System.out.println("true");
			
		}
		if (a<b) {
			System.out.println("true");
			
		}else {
			System.out.println("false");
		}

	}

}
