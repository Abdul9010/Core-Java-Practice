
public class Explicit {

	public static void main(String[] args) {
		
		double d1 = 10;
		float f1 = (float) d1;
		System.out.println(f1);
		
		float f2 = 20;
		long l1 = (long)  f2;
		System.out.println(l1);
		
		long l2 = 30;
		int i1 = (int) l2;
		System.out.println(i1);
		
		
		int i2 = 40;
		short s1 = (short)i2;
		System.out.println(s1);
		
		short s2 = 50;
		byte b1 = (byte)s2;
		System.out.println(b1);
		
	}

}
