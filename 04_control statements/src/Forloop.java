
public class Forloop {

	public static void main(String[] args) {
		
		for (int i = 0; i < 10; i++) {
			System.out.println(i);
		
		}
		for (int i = 10; i > 5; i--) {
			System.out.println(i);
			
		}
		int a[] = {10, 20, 30, 40};
		
		for (int i = 0; i < a.length; i++) {
			System.out.println(a[i]);
			
		}

		
	}
}

